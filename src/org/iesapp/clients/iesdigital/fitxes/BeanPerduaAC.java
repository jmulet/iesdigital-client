package org.iesapp.clients.iesdigital.fitxes;

/**
 *
 * @author Josep
 */
public class BeanPerduaAC {
    protected String nomAlumne;
    protected String grup;
    protected String motiu;
    protected String data;

    /**
     * @return the nomAlumne
     */
    public String getNomAlumne() {
        return nomAlumne;
    }

    /**
     * @param nomAlumne the nomAlumne to set
     */
    public void setNomAlumne(String nomAlumne) {
        this.nomAlumne = nomAlumne;
    }

    /**
     * @return the grup
     */
    public String getGrup() {
        return grup;
    }

    /**
     * @param grup the grup to set
     */
    public void setGrup(String grup) {
        this.grup = grup;
    }

    /**
     * @return the motiu
     */
    public String getMotiu() {
        return motiu;
    }

    /**
     * @param motiu the motiu to set
     */
    public void setMotiu(String motiu) {
        this.motiu = motiu;
    }

    /**
     * @return the data
     */
    public String getData() {
        return data;
    }

    /**
     * @param data the data to set
     */
    public void setData(String data) {
        this.data = data;
    }

}
