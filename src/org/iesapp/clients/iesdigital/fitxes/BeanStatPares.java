package org.iesapp.clients.iesdigital.fitxes;

/**
 *
 * @author Josep
 */
public class BeanStatPares {
    private String tutor;
    private String grup;
    private int nalumnes;
    private String entrevistes;
    private String entrevistats;

    /**
     * @return the tutor
     */
    public String getTutor() {
        return tutor;
    }

    /**
     * @param tutor the tutor to set
     */
    public void setTutor(String tutor) {
        this.tutor = tutor;
    }

    /**
     * @return the grup
     */
    public String getGrup() {
        return grup;
    }

    /**
     * @param grup the grup to set
     */
    public void setGrup(String grup) {
        this.grup = grup;
    }

    /**
     * @return the nalumnes
     */
    public int getNalumnes() {
        return nalumnes;
    }

    /**
     * @param nalumnes the nalumnes to set
     */
    public void setNalumnes(int nalumnes) {
        this.nalumnes = nalumnes;
    }

    /**
     * @return the entrevistes
     */
    public String getEntrevistes() {
        return entrevistes;
    }

    /**
     * @param entrevistes the entrevistes to set
     */
    public void setEntrevistes(String entrevistes) {
        this.entrevistes = entrevistes;
    }

    /**
     * @return the entrevistats
     */
    public String getEntrevistats() {
        return entrevistats;
    }

    /**
     * @param entrevistats the entrevistats to set
     */
    public void setEntrevistats(String entrevistats) {
        this.entrevistats = entrevistats;
    }

}
